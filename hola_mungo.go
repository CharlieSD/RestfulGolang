package main

import (
	"fmt"
)

func main() {
	var numero1 = 10.0
	var numero2 = 6.0

	fmt.Print("La suma es: ")
	fmt.Println(numero1 + numero2)

	fmt.Print("La resta es: ")
	fmt.Println(numero1 - numero2)

	fmt.Print("La multiplicación es: ")
	fmt.Println(numero1 * numero2)

	fmt.Print("La división es: ")
	fmt.Println(numero1 / numero2)
}
